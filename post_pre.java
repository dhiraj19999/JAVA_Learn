


class post_pre{

public static void main(String args[])

{
    int c =10;
    int y=6;
    System.out.println(c++) ;// Post method  ans will be 10
    System.out.println(++c) ;// pre method ans will be 11
    System.out.println(y<<3);// ans will be 2*2*2 three times multiple by 6=48 shift opreator 
    System.out.println(y>>3);// ans will be 2*2*2 three times divided by 6 = 8/3=2 shift opreator

}
}